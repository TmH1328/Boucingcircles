package model;

/**
 *
 * @author LENOVO
 */
public class Circle {
    private int x;
    private int y;
    private int spdX;
    private int spdY;
    private int r;

    public int getSpdX() {
        return spdX;
    }

    public void setSpdX(int spdX) {
        this.spdX = spdX;
    }

    public int getSpdY() {
        return spdY;
    }

    public void setSpdY(int spdY) {
        this.spdY = spdY;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getR() {
        return r;
    }

    public void setR(int r) {
        this.r = r;
    }

    
    
    
}
